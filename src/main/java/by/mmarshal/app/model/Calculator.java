package by.mmarshal.app.model;

public class  Calculator {
     public static double toPlus (double num1, double num2){
         return num1 + num2;
     }

    public static double toMinus (double num1, double num2){
        return num1 - num2;
    }

    public static double toMultiply (double num1, double num2){
        return num1 * num2;
    }

    public static double toSplit (double num1, double num2){
        return num1 / num2;
    }


 }
